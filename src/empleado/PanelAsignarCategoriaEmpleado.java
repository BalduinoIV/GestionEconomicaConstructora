    private void tblEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadosMouseClicked
        if (evt.getButton() == 1) {
            int fila = tblEmpleados.getSelectedRow();
            Empleado empleado = new Empleado();
            try {
                empleado = dba.PersonaDba.getEmpleado(Integer.parseInt(String.valueOf(tblEmpleados.getValueAt(fila, 0))));
            } catch (SQLException ex) {
                System.out.println(ex);
            }

            try {
                jComboBox2.getSelectedItem();
                jComboBox2.setSelectedItem(empleado.getCategoria().getNombre());
                habilitar();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "No se ha podido leer la informaci√≥n en la BD");
            }
        }
    }//GEN-LAST:event_tblEmpleadosMouseClicked

