    public void llenar() {
        String titulos[] = {"ID","Empleado", "Categoria"};
        modelo = new DefaultTableModel(null, titulos);

        String fila[] = new String[3];
        ArrayList<Empleado> empleados = new ArrayList<>();
        try {
            empleados = dba.PersonaDba.getEmpleados();
        } catch (SQLException ex) {
            System.out.println(ex);
        }

        for (Empleado empleado : empleados) {
            fila[0] = String.valueOf(empleado.getPK_ID());
            fila[1] = String.valueOf(empleado.getNombre() + " " + empleado.getApellidos());
            fila[2] = String.valueOf(empleado.getCategoria().getNombre());
            
            modelo.addRow(fila);
        }

        tblEmpleados.setModel(modelo);
    }
    
    public void llenarComboBox(){
        ArrayList<Categoria> categorias = null;
        try {
            categorias = dba.CategoriaDba.getCategorias();
        } catch (SQLException ex) {
            Logger.getLogger(PanelAsignarCategoriaEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for(Categoria c: categorias){
           model.addElement(c.getNombre());
        }
        jComboBox2.setModel(model);
    }
    
    public void modificar() {
        int fila = tblEmpleados.getSelectedRow();
        String identificador = (String) tblEmpleados.getValueAt(fila, 0);
        
        Empleado empleado = new Empleado();

        try {
            Categoria categoria = dba.CategoriaDba.getCategoria((String) jComboBox2.getSelectedItem());
            empleado.setCategoria(categoria);
            empleado.setPK_ID(Integer.parseInt(identificador));

            if (dba.PersonaDba.updateCategoriaEmpleado(empleado)) {
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }else{
                JOptionPane.showMessageDialog(null, "Fallo");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }

    }
    
